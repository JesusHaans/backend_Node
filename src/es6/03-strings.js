let hello = 'hello';
let world = 'world';

let greeting = hello + ' ' + world + '!'; // hello world!

console.log(greeting);

// Template literals

let greeting2 = `${hello} ${world}!`; // hello world!

// Multiline strings

let lorem = 'Lorem ipsum dolor sit amet, consectetur adipiscing elit. \n' + 'Vestibulum nec odio ipsum.'; // multiline

let lorem2 = `Lorem ipsum dolor sit amet, consectetur adipiscing elit.
Vestibulum nec odio ipsum.`; // multiline

console.log(lorem);
console.log(lorem2);